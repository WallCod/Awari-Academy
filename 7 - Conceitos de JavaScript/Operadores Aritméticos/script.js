//Primeiro declaramos 2 variáveis

let a = 10;
let b = 20;

// Usando o operador + (Adição)
let soma = a + b;
console.log("A soma de a + b é", soma);

// Usando o operador - (Subtração)
let subtracao = a - b;
console.log("A subtração de a - b é", subtracao);

// Usando o operador * (Multiplicação)
let multiplicacao = a * b;
console.log("A multiplicação de a * b é", multiplicacao);

// Usando o operador / (Divisão)
let divisao = a / b;
console.log("A divisão de a / b é", divisao);

// Usando o operador % (Modúlo ou Resto)
let resto = a % b;
console.log("O resto de a % b é", resto);

// Usando o operador ** (Potência OU Exponenciação)
let potencia = a ** b;
console.log("A Potência de a ** b é", potencia);

// Usando o operador ++ (Incremento)
let incremento = a;
a++; // Incrementa o valor de 'a' em 1
console.log("O valor de a após o incremento é", a);

// Usando o operador -- (Decremento)
let decremento = a;
a--; // Decrementa o valor de 'a' em 1
console.log("O valor de a após o decremento é", a);

