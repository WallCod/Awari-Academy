let number = 0;

if (number > 0) {
    console.log("O número é positivo.");
} else if (number < 0) {
    console.log("O número é negativo.");
} else {
    console.log("O número é zero.");
}

//Neste exemplo, o código verifica se a variável numero é positiva, negativa ou zero.
//A palavra-chave if é seguida por uma condição entre parênteses.
// Se a condição for verdadeira, o código dentro das chaves {} após o if é executado.
//Se a condição for falsa, o código passa para a próxima condição após a palavra-chave else if.
//Se nenhuma das condições anteriores for verdadeira, o código dentro das chaves {} após o else é executado.