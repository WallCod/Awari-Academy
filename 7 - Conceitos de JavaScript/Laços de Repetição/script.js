// While

// Este laço é usado quando você quer que o código seja executado até que uma certa condição seja falsa.
// Aqui está um exemplo:

let i = 0;
while (i < 5) {
    console.log("Número" + i);
    i++;
}

// Neste exemplo, o código dentro do laço while será executado até que i seja igual a 5, imprimindo os números de 0 a 4.



// Do While      

// Este laço é semelhante ao laço while, mas a condição é verificada após a execução do bloco de código, 
//garantindo que o bloco de código seja executado pelo menos uma vez.
// Aqui está um exemplo:


let a = 0;
do {
    console.log("Número " + a);
    a++;
} while (a < 6);



// For

//Este laço é usado quando você sabe quantas vezes deseja que o código seja executado.
// Aqui está um exemplo em JavaScript:

for (let k = 0; k < 5; k++) {
    console.log("10" + k);
}

// Neste exemplo, o código dentro do laço for será executado 5 vezes, e imprimirá os números de 0 a 4.






