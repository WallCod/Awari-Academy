// Criando uma array
let frutas = ["maçã", "banana", "manga", "uva", "morango"];

//Acessando os elementos da arrays
console.log("Primeira fruta:" + frutas[0]);
console.log("Segunda fruta:" + frutas[1]);

//Adicinando um elemento a array
frutas.push("laranja");
console.log("Array após adicionar uma fruta:" + frutas);

//Removendo o ultimo elemento do array
frutas.pop();
console.log("Array após remover uma fruta:" + frutas);